int ft_strncmp(char *s1, char *s2, int n)
{
    int size;

    size = 0;
    while (*s1 != '\0' && *s2 != '\0' && size < n)
    {
        if (*s1 != *s2)
        {
            break;
        }
        s1++;
        s2++;
        size++;
    }
    if (size == n)
        return (0);
    return (unsigned char) *s1 - (unsigned char) *s2;
}